public class Area {
    static float circle(float radius) {
        return radius * radius * 3.14f;
    }

    static float triangle(float a, float b) {
        return a * b * .5f;
    }

    static float rectangle(float a, float b) {
        return a * b;
    }

    static float square(float a) {
        return a * a;
    }
}

